- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    NSManagedObject *object = [self.fetchedResultsController objectAtIndexPath:indexPath];
    NSString* content = [[object valueForKey:@"content"] description];
    self.allVC.vc_favweb.text = content;
    [self.allVC.vc_favweb reloadWebView];
   [self.navigationController pushViewController:self.allVC.vc_favweb animated:TRUE];
}

